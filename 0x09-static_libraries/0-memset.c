/**
 * _memset - Program fills a block of memory with a specific value
 * @b: the desired value
 * @n: number of bytes to be changed
 *
 * Return: change array with new value for a bytes
 */
char *_memset(char *a, char b, unsigned int n)
{
	int n = 0;
	for (; n> 0; n++)
	{
		a(n)= b;
		n--;
	}
	return (0);
}
